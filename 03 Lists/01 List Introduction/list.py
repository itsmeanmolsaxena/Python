# create a list
# a list can hold any type of value and it is mutable means it's content can be changed

#integer list
int = [1, 2, 3, 4, 5]
print(int)

#character list
char = ['a', 'e', 'i', 'o', 'u']
print(char)

#string list
str = ["This", "is", "a", "string", "list"]
print(str)

#mixed list
mix = [1, 'a', "List"]
print(mix)

#list in a form of matrix
mat = [[1,2], ['x', 'y', 'z'], ["List"]]
print(mat)